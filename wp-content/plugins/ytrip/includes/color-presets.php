function ytrip_get_active_color_preset() {
    $options = get_option('ytrip_settings');
    $preset_key = isset( $options['color_preset'] ) ? $options['color_preset'] : 'preset_1';
    
    if ( $preset_key === 'custom' && isset( $options['custom_colors'] ) ) {
        return $options['custom_colors']; 
    }
    
    $presets = ytrip_get_color_presets();
    return isset( $presets[$preset_key] ) ? $presets[$preset_key]['colors'] : $presets['preset_1']['colors'];
}
